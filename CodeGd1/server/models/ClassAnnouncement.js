const mongoose = require('mongoose');

const ClassAnnouncementSchema = new mongoose.Schema(
  {
    classroomId: {
      type: mongoose.Schema.Types.ObjectId,
      ref: 'Classroom',
      required: true,
    },
    teacherId: {
      type: mongoose.Schema.Types.ObjectId,
      ref: 'User',
      required: true,
    },
    teacherName: {
      type: String,
      required: true,
      trim: true,
    },
    content: {
      type: String,
      required: true,
      trim: true,
    },
  },
  { timestamps: true }
);

ClassAnnouncementSchema.index({ classroomId: 1, createdAt: -1 });

module.exports = mongoose.model('ClassAnnouncement', ClassAnnouncementSchema);